namespace GetSetExercicio
{
    class Retangulo
    {
        private double _largura;
        private double _altura;

        public double GetLargura()
        {
            return _largura;
        }
        public void SetLargura(double largura)
        {
            _largura = largura;
        }

        public double GetAltura()
        {
            return _altura;
        }

        public void SetAltura(double altura)
        {
            _altura = altura;
        }
    }
}
